package org.newdawn.loaders;

/**
 * Exception thrown when some data in invalid
 *
 * @author $Author: jeremy $
 * @version $Revision: 1.1 $
 */
public class InvalidDataException extends java.lang.Exception {

    /**
     * Constructs an <code>InvalidDataException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public InvalidDataException(String msg) {
        super(msg);
    }
}


